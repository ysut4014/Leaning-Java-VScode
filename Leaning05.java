public class Leaning05 {
    public static void main(String[] args) {
        double pi = 3.14;
        int pie = 5;
        System.out.println("半径" + pie + "cmのπの面積は面積は、");
        System.out.println(pie * pie * pi);
        System.out.println("πの半径を倍にします");
        pi = 10;
        System.out.println("半径" + pie + "cmのπの面積は、");
        System.out.println(pie * pie * pi);
    }
}
